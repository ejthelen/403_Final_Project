package com.example.a403finalproject;

public class Walker {

    String name;
    String sDesc;
    double distanceAway;
    double Charge;
    String lDesc;
    String PhoneNumber;
    String Email;
    String Schedule;


    public Walker(String name, String sDesc, double distanceAway, double Charge, String lDesc, String phoneNumber, String email, String schedule) {
        this.name = name;
        this.sDesc = sDesc;
        this.distanceAway = distanceAway;
        this.Charge = Charge;
        this.lDesc = lDesc;
        PhoneNumber = phoneNumber;
        Email = email;
        Schedule = schedule;
    }
}
